package com.vectorx.pattern.t10_flyweight;

/**
 * 外部状态，非共享角色
 */
public class User {
    private String name;

    public User(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
